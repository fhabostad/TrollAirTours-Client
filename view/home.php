                            <h2> Briksdalen </h2>
                            <p> In Briksdalen lies Briksdalsbreen (the Briksdal glacier) and is one of the most accessible and best known arms of the Jostedalsbreen glacier. Briksdalsbreen is located in the municipality of Stryn in Sogn og Fjordane county, Norway. 
                            <p> It is located inside Jostedalsbreen National Park. Briksdalsbreen terminates in a small glacial lake, Briksdalsbrevatnet, which lies 346 metres (1,135 ft) above sea level.</p>
